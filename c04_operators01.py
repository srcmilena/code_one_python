# math operators (arithmetics)

"""
- sum: +
- minus: -
- division: /
- entire division: // (discard decimals e não arredonda)
- resto of division: %
- potency: **
"""

number1 = 10
number2 = 20

print(number1 + number2)
print(number1 - number2)
print(number1 * number2)
print(number1 / number2)
print(number1 // number2)
print(20 // 3) #20/3 = 6.67
print(20 % 3) #20 /3 = 6.67, mas o resto do cálculo é 2
print(2 ** 3)
